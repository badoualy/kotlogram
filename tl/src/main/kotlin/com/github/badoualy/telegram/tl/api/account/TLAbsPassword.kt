/**
 * Abstraction level for the following constructors:
 * * [account.noPassword#5ea182f6][TLNoPassword]
 * * [account.password#ca39b447][TLPassword]
 *
 * @author Yannick Badoual yann.badoual@gmail.com
 * @see <a href="http://github.com/badoualy/kotlogram">http://github.com/badoualy/kotlogram</a>
 */
abstract class TLAbsPassword : TLObject() {
    abstract var newSalt: TLBytes

    abstract var newSecureSalt: TLBytes

    abstract var secureRandom: TLBytes

    abstract var emailUnconfirmedPattern: String
}
