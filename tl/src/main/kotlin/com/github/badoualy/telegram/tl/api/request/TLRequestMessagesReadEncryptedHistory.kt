class TLRequestMessagesReadEncryptedHistory() : TLMethod<TLBool>() {
    var peer: TLInputEncryptedChat = TLInputEncryptedChat()

    var maxDate: Int = 0

    private val _constructor: String = "messages.readEncryptedHistory#7f4b690a"

    override val constructorId: Int = CONSTRUCTOR_ID

    constructor(peer: TLInputEncryptedChat, maxDate: Int) : this() {
        this.peer = peer
        this.maxDate = maxDate
    }

    @Throws(IOException::class)
    override fun serializeBody(tlSerializer: TLSerializer) = with (tlSerializer)  {
        writeTLObject(peer)
        writeInt(maxDate)
    }

    @Throws(IOException::class)
    override fun deserializeBody(tlDeserializer: TLDeserializer) = with (tlDeserializer)  {
        peer = readTLObject<TLInputEncryptedChat>(TLInputEncryptedChat::class, TLInputEncryptedChat.CONSTRUCTOR_ID)
        maxDate = readInt()
    }

    override fun computeSerializedSize(): Int {
        var size = SIZE_CONSTRUCTOR_ID
        size += peer.computeSerializedSize()
        size += SIZE_INT32
        return size
    }

    override fun toString() = _constructor

    override fun equals(other: Any?): Boolean {
        if (other !is TLRequestMessagesReadEncryptedHistory) return false
        if (other === this) return true

        return peer == other.peer
                && maxDate == other.maxDate
    }
    companion object  {
        const val CONSTRUCTOR_ID: Int = 0x7f4b690a.toInt()
    }
}
